package com.ipartek.formacion.basicos.estructura_iterativa;

/*
 * Programa Java que muestre los números del 100 al 1 utilizando la instrucción while.
 */
public class Ejercicio4 {
	
	public static void main(String[] args) {
		
		int i = 100;
		
		while (i>=0) {
			System.out.print(i + " ");
			i--;
		}
	}

}
